const LanguageContext = createContext(null);

function lookup(source, key) {
  return key.split('.').reduce((current, part) => (current && current[part] !== undefined ? current[part] : undefined), source);
}

function interpolate(value, vars = {}) {
  if (typeof value !== 'string') return value;
  return value.replace(/\{(\w+)\}/g, (_, key) => (vars[key] === undefined ? '' : String(vars[key])));
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return LANGUAGES.some((item) => item.code === saved) ? saved : 'zh-CN';
  });

  useEffect(() => {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = HTML_LANG[language] || 'zh-Hans';
  }, [language]);

  const get = useCallback(
    (key, fallback) => {
      const translated = lookup(dictionaries[language], key);
      if (translated !== undefined) return translated;
      const simplified = lookup(dictionaries['zh-CN'], key);
      return simplified !== undefined ? simplified : fallback;
    },
    [language],
  );

  const t = useCallback(
    (key, vars = {}, fallback = key) => {
      const value = get(key, fallback);
      return interpolate(value, vars);
    },
    [get],
  );

  const value = useMemo(() => ({ language, setLanguage, t, get }), [language, setLanguage, t, get]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useI18n() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useI18n must be used inside LanguageProvider');
  }
  return context;
}
